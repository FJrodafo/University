#!/bin/bash

# Write a shell script in that asks you to pick a color from Rainbow_colors.sh and print it.
# Print the specific element of the array corresponding to the desired color.

rainbow=("violet" "indigo" "blue" "green" "yellow" "orange" "red")
echo "Colors of the rainbow:" ${rainbow[*]}
echo "First color on this rainbow array:" ${rainbow[0]}
echo "Second color on this rainbow array:" ${rainbow[1]}
echo "Third color on this rainbow array:" ${rainbow[2]}
echo "Fourth color on this rainbow array:" ${rainbow[3]}
echo "Fifth color on this rainbow array:" ${rainbow[4]}
echo "Sixth color on this rainbow array:" ${rainbow[5]}
echo "Seventh color on this rainbow array:" ${rainbow[6]}
