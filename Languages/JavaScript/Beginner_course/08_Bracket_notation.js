// Bracket notation to find first character in string
var firstLetterOfFirstName = "";
var firstName = "Alba";

firstLetterOfFirstName = firstName[0];
console.log(firstLetterOfFirstName); // A

// Bracket notation to find fifth character in string
var fifthLetterOflastName = "";
var lastName = "Lovelace";

fifthLetterOflastName = lastName[4];
console.log(fifthLetterOflastName); // l

// Bracket notation to find last character in string
var myName = "Fran";
var lastLetterOfMyName = myName[myName.length - 1];
console.log(lastLetterOfMyName); // n

// Bracket notation to find nth-to-last character in string
var secondToLastLetterOfMyName = myName[myName.length - 2];
console.log(secondToLastLetterOfMyName); // a
