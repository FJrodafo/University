// Adding numbers
var sum = 10 + 10;
console.log(sum); // 20

// Subtracting numbers
var difference = 45 - 33;
console.log(difference); // 12

// Multiplying numbers
var product = 8 * 10;
console.log(product); // 80

// Dividing numbers
var quotient = 66 / 33;
console.log(quotient); // 2

// Finding a remainder
var remainder = 11 % 3;
console.log(remainder) // 2

// Incrementing numbers
var increment = 87;
console.log(increment++); // 88

// Decrementing numbers
var decrement = 11;
console.log(decrement--); // 10

// Decimal numbers
var firstDecimal = 0.009;
var secondDecimal = 5.7;

// Augmented math operations
var a = 3;
var b = 170;
var c = 12;
var d = 100;
var e = 11;

a += 2; // a = a + 2;
b -= 9; // b = 9 - b;
c *= 7; // c = c * 7;
d /= 4; // d = d / 4;
e %= 3; // e = e % 3;
