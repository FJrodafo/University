var myName1 = "Fran" // `var` is function-scoped, allowing variables to be accessed within the function where they are declared, regardless of block scope.

let myName2 = "Jose" // `let` is block-scoped, allowing variables to be accessed only within the block, statement, or expression where they are defined.

const PI = 3.14 // `const` is block-level scope, just like `let`. However, variables declared with `const` cannot be reassigned after their initial value is set, making them constant throughout the program.
