package Introduccion_a_JAVA.Ejemplos;

public class Unicode {
    public static void main(String[] args) {

        char caracter1 = '\u0045'; // E
        System.out.println(caracter1);

        char caracter2 = '\u03c0'; // π
        System.out.println(caracter2);

        char caracter3 = '\u00a9'; // ©
        System.out.println(caracter3);

        char caracter4 = '\u00ae'; // ®
        System.out.println(caracter4);

        char caracter5 = '\u2605'; // ★
        System.out.println(caracter5);

        char caracter6 = '\u2661'; // ♡
        System.out.println(caracter6);

        char caracter7 = '\u221e'; // ∞
        System.out.println(caracter7);
    }
}
